package roomChallenge;

public class pillows {
	private String pillowShape;
	private String pillowType;
	private int pillowNumber;
	public pillows(String pillowShape, String pillowType, int pillowNumber) {
		this.pillowShape = pillowShape;
		this.pillowType = pillowType;
		this.pillowNumber = pillowNumber;
	}
	public String getPillowShape() {
		return pillowShape;
	}
	public String getPillowType() {
		return pillowType;
	}
	public int getPillowNumber() {
		return pillowNumber;
	}
	
	
	

}
